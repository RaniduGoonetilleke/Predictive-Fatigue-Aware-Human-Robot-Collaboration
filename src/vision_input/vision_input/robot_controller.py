    def _send_gazebo_home(self):
        if self._gazebo_home_sent:
            return
        if not self.joint_state_received:
            return
        self._gazebo_home_sent = True
        self.set_joint_target_from_list(self.home_joints)
        traj = JointTrajectory()
        traj.joint_names = self.ur_joint_names
        point = JointTrajectoryPoint()
        point.positions = list(self.home_joints)
        point.velocities = [0.0] * 6
        point.time_from_start = Duration(sec=4, nanosec=0)
        traj.points = [point]
        self.real_robot_pub.publish(traj)
        self.get_logger().info("GAZEBO: Sent home position command")


