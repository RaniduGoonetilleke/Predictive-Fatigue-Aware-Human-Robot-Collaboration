    # IK Solver
    def solve_and_update(self, x, y, z):
        # Store the desired wrist xyz; the smooth tracker will interpolate
        # toward it in TASK space and call _ik_xyz_to_joints each tick.
        # IMPORTANT: do NOT initialize _task_smooth_xyz here: let the
        # smoother seed it from the actual robot TF, otherwise the smoother
        # starts at the target and there is nothing to interpolate.
        self._task_target_xyz = (x, y, z)
        self._ik_xyz_to_joints(x, y, z, self.target_pos, log_tag="REQ")
        return

    def _ik_xyz_to_joints(self, x, y, z, out, log_tag="OK "):
        """Analytical UR3 IK: writes 6 joint values into `out`."""
        wrist_x = x
        wrist_y = y
        wrist_z = z

        # UR3 DH parameters (we only need d1, d4 for the analytical IK).
        d1 = 0.1519
        d4 = 0.11235
        L1 = 0.24365   # |a2|
        L2 = 0.21325   # |a3|

        # Joint 1 (shoulder pan): must account for the d4 lateral offset.
        # The wrist sits on a cylinder of radius d4 around the base z-axis,
        # so atan2(y,x) alone is wrong (and any target with sqrt(x²+y²)<d4
        # is physically unreachable).
        rho_sq = wrist_x*wrist_x + wrist_y*wrist_y
        min_rho = d4 + 0.005
        if rho_sq < min_rho * min_rho:
            # Push the target outward along its bearing, the closest
            # reachable point on the d4 forbidden cylinder.
            theta = math.atan2(wrist_y, wrist_x) if rho_sq > 1e-6 else 0.0
            wrist_x = min_rho * math.cos(theta)
            wrist_y = min_rho * math.sin(theta)
            rho_sq = min_rho * min_rho
        # shoulder_left configuration (matches hardcoded pickup joints)
        phi = math.atan2(wrist_y, wrist_x)
        psi = math.atan2(d4, math.sqrt(rho_sq - d4*d4))
        out[0] = phi - psi

        # Project the wrist into the arm plane (after subtracting d4 offset
        # and shifting to the shoulder origin at height d1).
        r = math.sqrt(rho_sq - d4*d4)
        z_local = wrist_z - d1
        d = math.sqrt(r*r + z_local*z_local)

        max_d = L1 + L2 - 0.005
        out_of_reach = False
        if d > max_d:
            # Z-priority: keep height fixed, only shrink horizontal reach
            r_max_sq = max_d**2 - z_local**2
            if r_max_sq > 0.001:
                r = math.sqrt(r_max_sq)
            else:
                # Z alone fills reach: fall back to proportional scale
                scale = max_d / d
                r *= scale
                z_local *= scale
            d = max_d
            out_of_reach = True
        d = max(0.05, d)

        try:
            cos_elbow = (d*d - L1*L1 - L2*L2) / (2 * L1 * L2)
            cos_elbow = max(-1.0, min(1.0, cos_elbow))
            q3 = math.acos(cos_elbow)
            # UR3 convention: q2 < 0 -> arm up.  Negate the standard
            # 2-link planar result to match UR3 URDF sign convention.
            target_angle = math.atan2(z_local, r)
            inner_offset = math.atan2(L2*math.sin(q3),
                                      L1 + L2*math.cos(q3))
            q2 = -(target_angle + inner_offset)
            out[1] = q2
            out[2] = q3
            out[3] = -math.pi/2 - q2 - q3   # gripper down
            out[4] = -math.pi/2
            out[5] = 0.0

            if self._ik_log is not None:
                if not hasattr(self, '_dbg_ik_count'):
                    self._dbg_ik_count = 0
                self._dbg_ik_count += 1
                if self._dbg_ik_count % 10 == 1:
                    fk_r = L1*math.cos(q2) + L2*math.cos(q2 + q3)
                    fk_z = d1 - L1*math.sin(q2) - L2*math.sin(q2 + q3)
                    arm_dir = out[0]
                    fk_x = fk_r*math.cos(arm_dir) - d4*math.sin(arm_dir)
                    fk_y = fk_r*math.sin(arm_dir) + d4*math.cos(arm_dir)
                    tag = "OOR" if out_of_reach else log_tag
                    self._ik_log.write(
                        f"[{tag}] xyz=({x:+.3f},{y:+.3f},{z:+.3f}) "
                        f"d={d:.3f} "
                        f"j=[{out[0]:+.2f},{out[1]:+.2f},{out[2]:+.2f},{out[3]:+.2f}] "
                        f"FK=({fk_x:+.3f},{fk_y:+.3f},{fk_z:+.3f})\n")
        except ValueError as e:
            self.get_logger().warn(f"IK failed: {e}")

    # Main update loop
    def update_loop(self):
        self._publish_mode_marker()
        # DIRECT GAMEPAD TORCH (Scenario 1, sim + real) 
        # Sticks drive _torch_x/y/z via _torch_joy_cb; IK runs here and
        # the trajectory goes straight to the controller. Hoisted out of
        # the use_gazebo_sim branch so real-hardware torch isn't silently
        # skipped: previously this lived only inside the sim path.
        # DIRECT GAMEPAD TORCH: sandbox-identical path
        # Matches gamepad_sandbox.py exactly: deadman guard -> EMA ->
        # IK -> 50ms trajectory. No TF seed, no hazard check in the
        # fast loop. Fatigue alpha is the only addition.
        if (not self.use_tf_tracking and self._torch_override
                and self.joint_state_received and self.robot_ready):
            self.robot_state = "TORCH"

            if not self._torch_deadman or self.frozen:
                self._torch_seeded = False
                self._torch_aligned = False
                self._torch_align_start_time = None
                if self._fwd_ctrl_active:
                    self._switch_to_traj_ctrl()
                return

            # Bug 2 fix: retry TF seed on failure. `_torch_seeded = True`
            # is now INSIDE the try block: if lookup_transform throws an exception
            # (TF buffer not yet populated), next tick will retry instead
            # of silently driving to the hardcoded default xyz.
            if not self._torch_seeded:
                try:
                    tf_t = self.tf_buffer.lookup_transform(
                        'base_link', 'wrist_1_link', rclpy.time.Time())
                    t = tf_t.transform.translation
                    self._torch_x = t.x
                    self._torch_y = t.y
                    self._torch_z = t.z
                    self._torch_smooth_x = t.x
                    self._torch_smooth_y = t.y
                    self._torch_smooth_z = t.z
                    self._torch_seeded = True
                except Exception:
                    return

            # Bug 1 fix: wrist alignment. The IK forces wrist_2=-pi/2 and
            # wrist_3=0. If the real robot's wrists aren't already there,
            # a 50ms trajectory would demand huge joint velocities -> the
            # pendant velocity-limit popup. Run a one-shot slow alignment
            # trajectory, then proceed to the fast stick-tracking loop.
            if not self._torch_aligned:
                aligned_pose = [0.0] * 6
                self._ik_xyz_to_joints(
                    self._torch_x, self._torch_y, self._torch_z,
                    aligned_pose, log_tag="ALIGN")
                max_diff = max(
                    abs(aligned_pose[i] - self.actual_robot_pos[i])
                    for i in range(6))

                if max_diff < self.TORCH_ALIGN_TOL:
                    self._torch_aligned = True
                    self.target_pos[:6] = aligned_pose[:6]
                else:
                    if self._torch_align_start_time is None:
                        self._torch_align_start_time = self.now()
                        traj = JointTrajectory()
                        traj.joint_names = self.ur_joint_names
                        pt = JointTrajectoryPoint()
                        pt.positions = [float(p) for p in aligned_pose]
                        pt.time_from_start = Duration(
                            sec=int(self.TORCH_ALIGN_DURATION),
                            nanosec=int((self.TORCH_ALIGN_DURATION % 1)
                                        * 1e9))
                        traj.points = [pt]
                        self.real_robot_pub.publish(traj)
                        self.get_logger().info(
                            f"TORCH: aligning wrists over "
                            f"{self.TORCH_ALIGN_DURATION:.1f}s "
                            f"(max_joint_diff={max_diff:.2f} rad)")
                        return

                    elapsed = self.now() - self._torch_align_start_time
                    if elapsed >= self.TORCH_ALIGN_DURATION:
                        self._torch_aligned = True
                        self.target_pos[:6] = aligned_pose[:6]
                        self.get_logger().info(
                            "TORCH: alignment complete — stick control engaged")
                    else:
                        return

            # Integrate stick deflections at the true 20Hz dt. Done here
            # (not in the joy callback) so the effective stick speed is
            # independent of the joy publish rate.
            dt = 0.05
            fatigue_damp = {"FRESH": 1.0, "MILD": 0.70,
                            "MODERATE": 0.40, "SEVERE": 0.15}
            damp = fatigue_damp.get(self.fatigue_level, 1.0)
            step = self._torch_stick_speed * damp * dt

            self._torch_y += -self._torch_stick_lx * step
            self._torch_x += -self._torch_stick_ry * step
            self._torch_z += self._torch_stick_lt * step
            self._torch_z -= self._torch_stick_rt * step

            # Clamp Z
            self._torch_z = max(self._torch_min_z,
                                min(self._torch_max_z, self._torch_z))
            # Outer: independent box clamp (no axis blending)
            self._torch_x = max(-0.35, min(0.35, self._torch_x))
            self._torch_y = max(-0.35, min(0.35, self._torch_y))
            # Inner: radial clamp to prevent base collision
            h = math.sqrt(self._torch_x ** 2 + self._torch_y ** 2)
            if h < self.MIN_REACH and h > 0.001:
                scale = self.MIN_REACH / h
                self._torch_x *= scale
                self._torch_y *= scale

            # Hazard zone check: push target out of zone, shake wrist.
            tgt_hazard, _, tgt_edge_x, tgt_edge_y = self.check_hazard_zones(
                self._torch_x, self._torch_y, self._torch_z)
            if tgt_hazard:
                self._torch_x = tgt_edge_x
                self._torch_y = tgt_edge_y
                self.start_headshake()
                self.robot_state = "HAZARD_DENIED"
                self._hazard_denied = True
            else:
                self._hazard_denied = False
                self.robot_state = "TORCH"

            alpha = 0.08
            if self.fatigue_level == "MILD":
                alpha = 0.05
            elif self.fatigue_level == "MODERATE":
                alpha = 0.03
            elif self.fatigue_level == "SEVERE":
                alpha = 0.0

            self._torch_smooth_x += alpha * (self._torch_x - self._torch_smooth_x)
            self._torch_smooth_y += alpha * (self._torch_y - self._torch_smooth_y)
            self._torch_smooth_z += alpha * (self._torch_z - self._torch_smooth_z)

            self._ik_xyz_to_joints(
                self._torch_smooth_x, self._torch_smooth_y, self._torch_smooth_z,
                self.target_pos, log_tag="TORCH")

            # Headshake must run AFTER IK so the wrist_3 wiggle overrides
            # what the IK solver wrote into target_pos[5].
            self.update_headshake()

            # Diagnostic: throttle-log stick + target deltas so we can see
            # if the fast loop is actually running and responding to input.
            if not hasattr(self, '_torch_log_count'):
                self._torch_log_count = 0
            self._torch_log_count += 1
            if self._torch_log_count % 10 == 0:  # every 0.5s
                self.get_logger().info(
                    f"TORCH fast: stick lx={self._torch_stick_lx:+.2f} "
                    f"ry={self._torch_stick_ry:+.2f} "
                    f"lt={self._torch_stick_lt:+.2f} "
                    f"rt={self._torch_stick_rt:+.2f} | "
                    f"xyz=({self._torch_x:+.3f},"
                    f"{self._torch_y:+.3f},{self._torch_z:+.3f}) "
                    f"smooth=({self._torch_smooth_x:+.3f},"
                    f"{self._torch_smooth_y:+.3f},{self._torch_smooth_z:+.3f})")

            if self.use_ur_driver and not self.use_gazebo_sim:
                # Real hardware: stream via forward_velocity_controller
                # P-controller with uniform scaling to preserve Cartesian path.
                if not self._fwd_ctrl_active:
                    self._switch_to_fwd_ctrl()
                msg = Float64MultiArray()
                Kp = 4.0
                # 1. Raw velocities from P-controller
                raw_velocities = []
                for i in range(6):
                    error = self.target_pos[i] - self.actual_robot_pos[i]
                    raw_velocities.append(error * Kp)
                # 2. Uniform scaling: preserve joint velocity ratios
                max_vel = max(abs(v) for v in raw_velocities)
                max_allowed = 0.5 * self._estop_scale
                if max_vel > max_allowed:
                    scale = max_allowed / max_vel
                    velocities = [v * scale for v in raw_velocities]
                else:
                    velocities = raw_velocities
                msg.data = velocities
                self.fwd_vel_pub.publish(msg)
                self._latest_fwd_vel_cmd = msg
            else:
                # Gazebo: use trajectory controller (already smooth)
                traj = JointTrajectory()
                traj.joint_names = self.ur_joint_names
                pt = JointTrajectoryPoint()
                pt.positions = [float(p) for p in self.target_pos[:6]]
                pt.time_from_start = Duration(sec=0, nanosec=50_000_000)
                traj.points = [pt]
                self.real_robot_pub.publish(traj)

            # Also keep the 0.5s-timer-driven hazard/fetch markers alive by
            # calling their impls directly: the update_loop early return
            # here skips the gesture-mode marker refresh, and on some
            # configurations the RViz markers visibly drop out.
            try:
                self._publish_hazard_zone_markers_impl()
                self._publish_fetch_object_markers_impl()
            except Exception:
                pass
            return

        # Outside direct gamepad mode: don't fall through to the
        # gesture/periodic-resend path which sends stale targets.
        if not self.use_tf_tracking and self._torch_override:
            try:
                self._publish_hazard_zone_markers_impl()
                self._publish_fetch_object_markers_impl()
            except Exception:
                pass
            return

        # GAZEBO FIX: Don't send any commands until robot is ready
        if self.use_gazebo_sim:
            if not self.joint_state_received:
                return

            # E-STOP: freeze everything: no movement, no fetch advance
            if self.frozen:
                return

            # VIRTUAL HAND MODES (sensor-agnostic)
            # Only active when use_tf_tracking:=true (Scenario 2 sandbox).
            # Scenario 1 (direct gamepad) skips this entire block.

            # FETCH MODE: ray hover (visual only) 
            # Virtual hand finger direction -> cyan highlight on cube.
            # Press OKAY to confirm fetch. No gesture gate, in TF
            # tracking the virtual hand IS always pointing by construction.
            if self.use_tf_tracking and not self._torch_override and self.fetch_state == "NONE":
                self._ray_hover_idx = self.find_raycast_object()
            elif self.fetch_state not in ("NONE",):
                # During active fetch, don't hover
                self._ray_hover_idx = -1
            else:
                self._ray_hover_idx = -1

            # TORCH MODE: robot follows fingertip 
            # (only in TF tracking mode, skip if fetch is active)
            _fetch_active = self.fetch_state not in ("NONE", "IDENTIFIED")
            _tf_track = False
            if self.use_tf_tracking and not _fetch_active and self._torch_override and self.current_gesture == "POINT" and not self.frozen:
                try:
                    tf_tip = self.tf_buffer.lookup_transform(
                        'world', 'index_distal', rclpy.time.Time())
                    t = tf_tip.transform.translation
                    self._torch_x = t.x
                    self._torch_y = t.y
                    self._torch_z = t.z
                    _tf_track = True
                except Exception:
                    pass

            if _tf_track:
                # Fatigue-adaptive smoothing: slower = safer when tired
                if self.fatigue_level == "FRESH":
                    alpha = 0.15
                elif self.fatigue_level == "MILD":
                    alpha = 0.08
                elif self.fatigue_level == "MODERATE":
                    alpha = 0.03
                else:  # SEVERE — lockout
                    alpha = 0.0

                # Polar EMA toward target: smooth (rho, theta) instead
                # of (x, y) so the path orbits the base dead-zone circle
                # instead of cutting through the origin, where atan2 is
                # unstable and the shoulder pan would flip.
                target_rho = math.sqrt(self._torch_x * self._torch_x
                                        + self._torch_y * self._torch_y)
                # Clamp target radius outside the IK dead-zone.
                target_rho = max(target_rho, self._torch_min_rho)
                if target_rho > 1e-6 and (self._torch_x * self._torch_x
                                          + self._torch_y * self._torch_y) > 1e-8:
                    target_theta = math.atan2(self._torch_y, self._torch_x)
                else:
                    # Finger exactly on base axis: keep current bearing.
                    target_theta = self._torch_smooth_theta

                # Angular EMA with shortest-arc wrap (prevents a ±2π
                # flip when the bearing crosses the ±π branch).
                dtheta = target_theta - self._torch_smooth_theta
                while dtheta > math.pi:
                    dtheta -= 2 * math.pi
                while dtheta < -math.pi:
                    dtheta += 2 * math.pi
                self._torch_smooth_theta += alpha * dtheta
                self._torch_smooth_rho += alpha * (target_rho - self._torch_smooth_rho)
                # Safety: never let smoothed radius drop into the dead-zone.
                self._torch_smooth_rho = max(self._torch_smooth_rho, self._torch_min_rho)
                self._torch_smooth_z += alpha * (self._torch_z - self._torch_smooth_z)

                # Reconstruct cartesian for hazard checks + IK.
                self._torch_smooth_x = self._torch_smooth_rho * math.cos(self._torch_smooth_theta)
                self._torch_smooth_y = self._torch_smooth_rho * math.sin(self._torch_smooth_theta)

                tx, ty, tz = self._torch_smooth_x, self._torch_smooth_y, self._torch_smooth_z

                # Hazard zone check: hand can go anywhere, robot is BLOCKED.
                # Check where the robot WOULD go (the target)
                tgt_hazard, _, tgt_edge_x, tgt_edge_y = self.check_hazard_zones(tx, ty, tz)
                if tgt_hazard:
                    # Clamp target to zone edge: robot cannot enter
                    tx, ty = tgt_edge_x, tgt_edge_y
                    self._torch_smooth_x = tgt_edge_x
                    self._torch_smooth_y = tgt_edge_y
                    # Keep polar state in sync or next tick will snap back.
                    self._torch_smooth_rho = max(
                        math.sqrt(tgt_edge_x * tgt_edge_x + tgt_edge_y * tgt_edge_y),
                        self._torch_min_rho)
                    self._torch_smooth_theta = math.atan2(tgt_edge_y, tgt_edge_x)
                    self.start_headshake()
                    self.robot_state = "HAZARD_DENIED"
                    self._hazard_denied = True
                else:
                    # Also check robot's actual position: push out if inside
                    try:
                        robot_tf = self.tf_buffer.lookup_transform(
                            'world', 'wrist_3_link', rclpy.time.Time())
                        rx = robot_tf.transform.translation.x
                        ry = robot_tf.transform.translation.y
                        in_hz, _, edge_x, edge_y = self.check_hazard_zones(rx, ry, 0.0)
                        if in_hz:
                            tx, ty = edge_x, edge_y
                            self._torch_smooth_x = edge_x
                            self._torch_smooth_y = edge_y
                            self._torch_smooth_rho = max(
                                math.sqrt(edge_x * edge_x + edge_y * edge_y),
                                self._torch_min_rho)
                            self._torch_smooth_theta = math.atan2(edge_y, edge_x)
                            self.start_headshake()
                            self.robot_state = "HAZARD_DENIED"
                            self._hazard_denied = True
                        else:
                            self._hazard_denied = False
                            if self.fetch_state == "NONE":
                                if self.robot_state != "TORCH":
                                    self._task_smooth_xyz = None
                                self.robot_state = "TORCH"
                    except Exception:
                        self._hazard_denied = False
                        if self.fetch_state == "NONE":
                            if self.robot_state != "TORCH":
                                self._task_smooth_xyz = None
                            self.robot_state = "TORCH"

                # IK solve: target is always outside the hazard zone
                self._ik_xyz_to_joints(tx, ty, tz, self.target_pos, log_tag="TORCH")

                # Apply headshake animation (wrist wiggle on hazard denial)
                self.update_headshake()

                # Send trajectory
                traj = JointTrajectory()
                traj.joint_names = self.ur_joint_names
                pt = JointTrajectoryPoint()
                pt.positions = [float(p) for p in self.target_pos[:6]]
                pt.time_from_start = Duration(sec=0, nanosec=50_000_000)
                traj.points = [pt]
                self.real_robot_pub.publish(traj)
                return

            # Direct-gamepad torch is now handled at the top of update_loop
            # so it works for sim and real hardware. D-pad fetch cycling
            # still lives in _torch_joy_cb. OKAY gesture confirms fetch.

            # Only move when explicitly commanded
            if self.current_gesture == "NEUTRAL" and self.fetch_state == "NONE" and not self.headshake_active:
                return

        self.update_headshake()

        if self.fetch_state in ["HOMING_TO_FETCH", "PRE_APPROACHING", "APPROACHING", "GRASPING", "RETURNING_HOME", "DELIVERING"]:
            self.advance_fetch_state()

        # ── Freeze arm during grasp: weld needs zero motion ──
        if self.fetch_state in ["GRASPING", "WAITING_GRASP"]:
            return

        # Link attachment is handled by single attach/detach calls in the
        # fetch state machine (IFRA LinkAttacher plugin), not per-tick teleport.

        # Joint limits for real robot only.
        # Gazebo relies on the workspace MIN_Z clamp instead, clamping joints
        # AFTER the IK breaks consistency and can put the arm underground.
        if not self.use_gazebo_sim:
            self.target_pos[0] = max(-2.35, min(self.target_pos[0], 2.35))
            self.target_pos[1] = max(-3.0,  min(self.target_pos[1], 0.0))
            self.target_pos[2] = max(0.0,   min(self.target_pos[2], 3.14))
            self.target_pos[3] = max(-2.5,  min(self.target_pos[3], 0.0))

        if self.use_ur_driver:
            if not self.robot_ready or not self.joint_state_received:
                return

            # E-STOP applies to both sim and real, without this the real
            # robot keeps firing 50ms trajectories that rubber-band the arm
            # back to the STOP-snapped target after it overshoots while braking.
            if self.frozen:
                return

            if self.use_gazebo_sim:
                # GAZEBO SMOOTH TRACKING PATH
                # Sends every timer tick (50ms) with a 50ms trajectory.
                # The trajectory always finishes exactly when the next one arrives
                # -zero preemption, zero jerk. Fatigue slows via alpha, not duration.

                # Fatigue-adaptive smoothing alpha (slower alpha = slower tracking)
                if self.fatigue_level == "FRESH":
                    effective_alpha = 0.18
                elif self.fatigue_level == "MILD":
                    effective_alpha = 0.10
                elif self.fatigue_level == "MODERATE":
                    effective_alpha = 0.04
                else:
                    effective_alpha = 0.0  # SEVERE lockout — smooth pos freezes

                # Soft floor stop: if the fingertip is at/below the table (z<0.02),
                # snap smooth_pos back to the actual robot position so the arm
                # stops drifting lower: but DON'T return.  The trajectory system
                # continues below and will move the arm upward if the gesture target
                # is now higher (user pointed up).
                try:
                    _tf = self.tf_buffer.lookup_transform(
                        'base_link', 'tool0', rclpy.time.Time())
                    _ftz = self._fingertip_from_tf(_tf)[2]
                    if _ftz < 0.02:
                        self.get_logger().warn(
                            f"FLOOR: fingertip z={_ftz:.3f}m — snapping smooth pos up")
                        self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                except Exception:
                    pass

                # Initialise smooth pos from actual robot pos on first run
                if self._gazebo_smooth_pos is None:
                    self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])

                # === TASK-SPACE SMOOTHING ===
                # Joint-space smoothing between two valid poses can pass
                # through subterranean intermediates. Instead, smooth the
                # wrist xyz, run IK each tick, and use those joints. This
                # forces the wrist to follow a straight-line task-space
                # trajectory that never dips below the table.
                live_tracking = (
                    self._task_target_xyz is not None
                    and self.fetch_state == "NONE"
                    and self.robot_state in ("TORCH", "MOVING", "DWELL_WAITING"))

                if live_tracking:
                    if self._task_smooth_xyz is None:
                        # Seed from wrist_1_link to match IK solver's frame
                        try:
                            _tf = self.tf_buffer.lookup_transform(
                                'base_link', 'wrist_1_link', rclpy.time.Time())
                            t = _tf.transform.translation
                            self._task_smooth_xyz = (t.x, t.y, t.z)
                        except Exception:
                            self._task_smooth_xyz = self._task_target_xyz
                    sx, sy, sz = self._task_smooth_xyz
                    tx, ty, tz = self._task_target_xyz
                    sx += effective_alpha * (tx - sx)
                    sy += effective_alpha * (ty - sy)
                    sz += effective_alpha * (tz - sz)
                    self._task_smooth_xyz = (sx, sy, sz)
                    self._ik_xyz_to_joints(sx, sy, sz, self.target_pos, log_tag="TASK")
                    # Use IK output directly: no joint-space smoothing
                    for i in range(6):
                        self._gazebo_smooth_pos[i] = self.target_pos[i]
                else:
                    # Fetch / non-tracking moves still use joint-space smoothing
                    # because they target hardcoded joint poses (no xyz to smooth)
                    for i in range(6):
                        self._gazebo_smooth_pos[i] += effective_alpha * (
                            self.target_pos[i] - self._gazebo_smooth_pos[i])

                    # Deadband only for fetch/non-tracking: once at a hardcoded
                    # waypoint we don't need to keep re-sending the same pose.
                    # Live tracking sends every tick (like the sandbox) for
                    # continuous silky motion.
                    smooth_change = max(
                        abs(self._gazebo_smooth_pos[i] - self.last_sent_target[i])
                        for i in range(6))
                    if smooth_change < 0.020 and not self.headshake_active:
                        return

                # 50ms trajectory = one timer tick, so next send arrives exactly
                # as this one ends -> perfect chaining, zero preemption.
                traj = JointTrajectory()
                traj.header.stamp.sec = 0
                traj.header.stamp.nanosec = 0
                traj.joint_names = self.ur_joint_names
                point = JointTrajectoryPoint()
                point.positions = list(self._gazebo_smooth_pos)
                point.velocities = [0.0] * 6
                point.time_from_start = Duration(sec=0, nanosec=50_000_000)
                traj.points = [point]
                self.real_robot_pub.publish(traj)
                self.last_sent_target = list(self._gazebo_smooth_pos)
                self.command_sent_time = self.now()
                if not hasattr(self, '_dbg_send_count'):
                    self._dbg_send_count = 0
                self._dbg_send_count += 1
                if self._dbg_send_count % 20 == 1:
                    sp = self._gazebo_smooth_pos
                    ap = self.actual_robot_pos
                    self.get_logger().info(
                        f"[SEND] smooth=[{sp[0]:.2f},{sp[1]:.2f},{sp[2]:.2f},"
                        f"{sp[3]:.2f},{sp[4]:.2f},{sp[5]:.2f}] "
                        f"actual=[{ap[0]:.2f},{ap[1]:.2f},{ap[2]:.2f},"
                        f"{ap[3]:.2f},{ap[4]:.2f},{ap[5]:.2f}]")

                # GAZEBO COLLISION / STALL DETECTION
                # If the robot can't reach its target for 2s it's probably blocked.
                # Skip during intentional fetch moves (those are slow by design).
                pos_error = max(
                    abs(self.actual_robot_pos[i] - self._gazebo_smooth_pos[i])
                    for i in range(6))
                in_fetch_move = self.fetch_state in ["HOMING_TO_FETCH", "PRE_APPROACHING", "APPROACHING", "RETURNING_HOME", "DELIVERING"]

                if pos_error > 0.25 and not in_fetch_move:
                    if self._collision_start_time is None:
                        self._collision_start_time = self.now()  # start timing the error
                    elif self.now() - self._collision_start_time > 0.5:
                        if not self.frozen:
                            self.get_logger().error(
                                f"SAFETY STOP: robot stuck (error={pos_error:.2f} rad) "
                                "— possible floor/object collision. Show OKAY to unfreeze.")
                            self.frozen = True
                            self.robot_state = "COLLISION_STOP"
                            for i in range(6):
                                self.target_pos[i] = self.actual_robot_pos[i]
                            self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                else:
                    # Error is acceptable: reset the stall timer
                    self._collision_start_time = None

                return

            # REAL ROBOT PATH
            # Option B: Webcam torch streaming bypass.
            # When the arm is in TORCH state driven by landmark_callback
            # (webcam hand tracking: NOT the gamepad velocity path), stream
            # 50ms trajectories every tick just like the Gazebo smooth path
            # so the arm follows the fingertip continuously. The 0.15 rad
            # deadband below is meant for the fetch state machine's discrete
            # waypoint jumps; webcam torch updates are small and smooth and
            # would never cross that threshold, leaving the arm frozen.
            # Gamepad torch (self._torch_override=True) is untouched: it
            # runs through the forward_velocity_controller path above.
            if (self.robot_state == "TORCH"
                    and not self._torch_override
                    and self.fetch_state == "NONE"
                    and not self.headshake_active):
                # Webcam torch: stream via forward_velocity_controller
                # (same P-loop the gamepad torch uses). JointTrajectory
                # with zero-velocity 50ms points caused violent stop/go
                # micro-stutter on real hardware; velocity streaming is
                # smooth because the UR controller integrates it directly.
                if not self._fwd_ctrl_active:
                    self._switch_to_fwd_ctrl()
                    self._torch_prev_vel = [0.0] * 6
                Kp = 4.0
                max_allowed = 0.5 * self._estop_scale
                # Slew cap (rad/s change per tick). Normal operation uses
                # 0.05 for smooth accel. During soft-stop we need a NON-ZERO
                # decel cap so the output actually walks down to 0, but
                # smaller than normal so the stop feels gentle, not jerky.
                # 0.015 ≈ 0.3 rad/s² at 20 Hz -> ~1.5 s from full 0.5 rad/s.
                if self._estop_scale < 0.5:
                    max_delta = 0.015
                else:
                    max_delta = 0.05
                raw_velocities = [
                    (self.target_pos[i] - self.actual_robot_pos[i]) * Kp
                    for i in range(6)
                ]
                max_vel = max(abs(v) for v in raw_velocities)
                if max_vel > max_allowed:
                    scale = max_allowed / max_vel
                    velocities = [v * scale for v in raw_velocities]
                else:
                    velocities = raw_velocities
                # Velocity slew rate limit: cap per-tick acceleration so
                # the first tick after entering TORCH doesn't step from 0
                # to full speed and trip the UR3 pendant watchdog.
                limited = []
                for prev, new in zip(self._torch_prev_vel, velocities):
                    diff = new - prev
                    if diff > max_delta:
                        diff = max_delta
                    elif diff < -max_delta:
                        diff = -max_delta
                    limited.append(prev + diff)
                velocities = limited
                self._torch_prev_vel = list(velocities)
                # slew_log.csv: single-joint row per tick (shoulder_lift).
                # v_raw = raw P-controller output, v_limited = post-slew cmd.
                if self._slew_csv_writer is not None:
                    _fatigue_map = {"FRESH": 1.0, "MILD": 0.625,
                                    "MODERATE": 0.375, "SEVERE": 0.0}
                    try:
                        self._slew_csv_writer.writerow([
                            f"{time.time() - self._slew_csv_start:.3f}",
                            f"{raw_velocities[1]:.4f}",
                            f"{limited[1]:.4f}",
                            f"{self._estop_scale:.3f}",
                            f"{_fatigue_map.get(self.fatigue_level, 1.0):.3f}",
                            "shoulder_lift",
                        ])
                        self._slew_csv.flush()
                    except Exception:
                        pass
                msg = Float64MultiArray()
                msg.data = velocities
                self.fwd_vel_pub.publish(msg)
                self._latest_fwd_vel_cmd = msg
                self.last_sent_target = list(self.target_pos[:6])
                self.command_sent_time = self.now()
                return

            target_change = max(
                abs(self.target_pos[i] - self.last_sent_target[i]) for i in range(6))
            robot_at_target = max(
                abs(self.actual_robot_pos[i] - self.last_sent_target[i]) for i in range(6))
            time_since_send = self.now() - self.command_sent_time  # GAZEBO FIX

            should_send = False
            reason = ""
            if target_change > 0.15:
                should_send = True
                reason = f"new target (change={target_change:.3f})"
            elif robot_at_target < 0.02 and target_change > 0.01:
                should_send = True
                reason = "robot arrived, small update"
            elif time_since_send > 10.0:
                should_send = True
                reason = "periodic resend"
            if self.headshake_active:
                should_send = True
                reason = "headshake animation"

            if not should_send:
                return

            # If we're about to send a discrete fetch trajectory but the
            # forward_velocity_controller is still active from TORCH mode,
            # hand control back to the trajectory controller, otherwise
            # fetch JointTrajectory commands are silently dropped
            # (HOMING_TO_FETCH shows but the arm doesn't move).
            if self._fwd_ctrl_active:
                self._switch_to_traj_ctrl()

            max_move = max(
                abs(self.target_pos[i] - self.actual_robot_pos[i]) for i in range(6))

            # Live-tunable float timing: pendant slider still clamps top speed.
            scale = self.get_parameter('fetch_speed_scale').value
            fmin  = self.get_parameter('fetch_min_time').value
            fmax  = self.get_parameter('fetch_max_time').value
            base_time = max(fmin, min(fmax, max_move * scale))

            if self.fatigue_level == "MILD":
                move_time = base_time * 1.5
            elif self.fatigue_level == "MODERATE":
                move_time = base_time * 2.5
            else:
                move_time = base_time  # FRESH or unknown
            if self.headshake_active:
                move_time = self.get_parameter('headshake_move_time').value

            move_sec = int(move_time)
            move_nanosec = int((move_time - move_sec) * 1e9)

            traj = JointTrajectory()
            traj.header.stamp.sec = 0
            traj.header.stamp.nanosec = 0
            traj.joint_names = self.ur_joint_names
            point = JointTrajectoryPoint()
            point.positions = list(self.target_pos[:6])
            point.velocities = [0.0] * 6
            point.time_from_start = Duration(sec=move_sec, nanosec=move_nanosec)
            traj.points = [point]
            self.real_robot_pub.publish(traj)
            self.last_sent_target = list(self.target_pos[:6])
            self.command_sent_time = self.now()  # GAZEBO FIX
            self.get_logger().info(
                f"COMMAND SENT: reason={reason}, move_dist={max_move:.3f}, "
                f"time={move_time:.2f}s")
        else:
            if self.fatigue_level == "FRESH":
                effective_alpha = self.alpha
            elif self.fatigue_level == "MILD":
                effective_alpha = self.alpha * 0.6
            elif self.fatigue_level == "MODERATE":
                effective_alpha = self.alpha * 0.35
            else:
                effective_alpha = 0.0

            for i in range(len(self.current_pos)):
                self.current_pos[i] += effective_alpha * (self.target_pos[i] - self.current_pos[i])
            if not self.use_gazebo_sim:
                msg = JointState()
                msg.header.stamp = self.get_clock().now().to_msg()
                msg.name = self.all_joint_names
                msg.position = self.current_pos
                self.joint_pub.publish(msg)

    # Gesture callback

    def gesture_callback(self, msg: String):
        gesture = msg.data
        self.current_gesture = gesture

        if gesture == "RESET_WORLD":
            self.reset_gazebo_objects()
            self.current_gesture = "NEUTRAL"
            return

        #    TOGGLE (shaka): deterministic FETCH↔TORCH handoff for webcam mode.
        #    Flips _torch_active (NOT _torch_override) to keep webcam state
        #    separate from the gamepad deadman path. Debounced so the operator
        #    must leave the gesture before re-toggling.
        if gesture == "TOGGLE":
            if not getattr(self, '_toggle_locked', False):
                self._torch_active = not self._torch_active
                self._dwell_start_time = None
                self._dwell_target = None

                # Sever any lingering FETCH lock the instant torch ignites,
                # otherwise a previously IDENTIFIED cube stays highlighted.
                if self._torch_active and self.fetch_state == "IDENTIFIED":
                    self.fetch_state = "NONE"
                    self.fetch_object_id = -1

                self._clear_prediction_markers()
                self.robot_state = "TORCH" if self._torch_active else "IDLE"
                self._toggle_locked = True

                self.get_logger().info(
                    f"MODE TOGGLED: Torch is now {self._torch_active}")

                # Force immediate UI redraws: bypasses the 0.5s timers so the
                # HUD/mode label/cube highlights repaint on the toggle tick.
                self._publish_mode_marker()
                self.publish_prediction_metrics()
                self.publish_fetch_object_markers()
            return
        else:
            self._toggle_locked = False

        #    Reset dwell on any gesture change away from POINT.
        #    _torch_active is deliberately NOT cleared here: once TOGGLE
        #    (shaka) latches torch on, the operator can GRASP/NEUTRAL without
        #    dropping out. STOP force-clears it above; another TOGGLE untoggles.
        if gesture != "POINT":
            self._dwell_start_time = None
            self._dwell_target = None
            self.pending_target = None

        if gesture == "STOP":
            self.frozen = True
            self.headshake_active = False
            self._torch_active = False  # STOP force-clears torch latch
            self._frozen_fetch_state = self.fetch_state
            self._frozen_fetch_object_id = self.fetch_object_id

            # Hard-brake: replace the active (possibly multi-second) trajectory
            # with a 100ms hold-current-position trajectory. Empty trajectories
            # are not reliably honoured by scaled_joint_trajectory_controller,
            # so we send an explicit "arrive at current pose with zero velocity
            # in 100ms" command. Zero velocities on fwd_vel_pub cover torch mode.
            if self.use_ur_driver and self.joint_state_received:
                stop_traj = JointTrajectory()
                stop_traj.joint_names = self.ur_joint_names
                pt = JointTrajectoryPoint()
                pt.positions = list(self.actual_robot_pos[:6])
                pt.velocities = [0.0] * 6
                pt.time_from_start = Duration(sec=0, nanosec=100_000_000)
                stop_traj.points = [pt]
                self.real_robot_pub.publish(stop_traj)

                zero_vel = Float64MultiArray()
                zero_vel.data = [0.0] * 6
                self.fwd_vel_pub.publish(zero_vel)
                self._latest_fwd_vel_cmd = None

            if self.joint_state_received:
                for i in range(6):
                    self.target_pos[i] = self.actual_robot_pos[i]
            else:
                self.target_pos = self.current_pos.copy()
            if self.use_gazebo_sim and self._gazebo_smooth_pos is not None:
                self._gazebo_smooth_pos = list(self.target_pos[:6])
            self._task_smooth_xyz = None
            self.robot_state = "FROZEN"
            self.latency_writer.writerow(['robot_frozen', 'FROZEN', self.now()])
            self.latency_log.flush()
            self.get_logger().warn("E-STOP — Robot FROZEN (press OKAY to resume)")
            return

        if not self.robot_ready:
            return

        if gesture == "OKAY":
            # Don't change gripper while carrying an object
            _carry_states = ["GRASPING", "RETURNING_HOME",
                             "DELIVERING", "WAITING_RECEIVE",
                             "APPROACHING", "WAITING_GRASP"]
            _saved = getattr(self, '_frozen_fetch_state', 'NONE')
            if self.fetch_state not in _carry_states and _saved not in _carry_states:
                self.target_pos[6] = 0.0
                self.command_gripper(0.0)

            if self.frozen:
                self.frozen = False
                # Resume paused fetch if there was one
                saved_fetch = getattr(self, '_frozen_fetch_state', 'NONE')
                if saved_fetch not in ('NONE', 'IDENTIFIED'):
                    self.fetch_state = saved_fetch
                    self.fetch_object_id = getattr(self, '_frozen_fetch_object_id', -1)
                    self.fetch_state_start_time = self.now()

                    # Restore the original destination target. Without this,
                    # target_pos still equals actual_pos (from the STOP snap),
                    # so advance_fetch_state sees max_err≈0 and silently
                    # advances past the state: e.g. DELIVERING ends at the
                    # freeze position and the cube drops there instead of at
                    # the handover pose.
                    if 0 <= self.fetch_object_id < len(self.registered_objects):
                        obj = self.registered_objects[self.fetch_object_id]
                        if saved_fetch in ("HOMING_TO_FETCH", "RETURNING_HOME"):
                            self.set_joint_target_from_list(self.home_joints)
                        elif saved_fetch == "PRE_APPROACHING":
                            if 'pre_approach_joints' in obj:
                                self.set_joint_target_from_list(obj['pre_approach_joints'])
                            else:
                                self.set_joint_target_from_list(obj['pickup_joints'])
                        elif saved_fetch == "APPROACHING":
                            self.set_joint_target_from_list(obj['pickup_joints'])
                        elif saved_fetch == "DELIVERING":
                            self.set_joint_target_from_list(obj['handover_joints'])

                    # Force the real-robot update_loop to send a fresh trajectory
                    # immediately. Without this, target_pos == last_sent_target
                    # (both point to the same destination saved before STOP), so
                    # the "new target" trigger doesn't fire and the arm waits up
                    # to 10s for the periodic-resend safety fallback.
                    self.last_sent_target = [0.0] * 6

                    # Re-sync smooth pos from actual robot position
                    if self.use_gazebo_sim and self._gazebo_smooth_pos is not None:
                        self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                    obj_name = ''
                    if 0 <= self.fetch_object_id < len(self.registered_objects):
                        obj_name = f" — resuming {self.registered_objects[self.fetch_object_id]['name']} fetch"
                    self.robot_state = "FETCH_RESUMED"
                    self.get_logger().info(f"OKAY — Robot UNFROZEN{obj_name}")
                else:
                    self.robot_state = "IDLE"
                    self.get_logger().info("OKAY — Robot UNFROZEN")
                self._frozen_fetch_state = 'NONE'
                self._frozen_fetch_object_id = -1
                return

            # Ray hover confirm: OKAY while pointing at a highlighted cube
            if self.fetch_state == "NONE" and self._ray_hover_idx >= 0:
                self.fetch_object_id = self._ray_hover_idx
                self._ray_hover_idx = -1  # Clear hover now that it's confirmed
                obj = self.registered_objects[self.fetch_object_id]
                self.set_joint_target_from_list(self.home_joints)
                self.fetch_state = "HOMING_TO_FETCH"
                self.robot_state = "FETCH_HOMING"
                self.fetch_state_start_time = self.now()
                if self.use_gazebo_sim and self._gazebo_smooth_pos is not None:
                    self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                self._task_smooth_xyz = None
                self._task_target_xyz = None
                self.get_logger().info(
                    f"FETCH CONFIRMED: Homing first, then picking up {obj['name']}")
                return

            # D-pad selected confirm (Scenario 1)
            if self.fetch_state == "IDENTIFIED":
                obj = self.registered_objects[self.fetch_object_id]
                self.set_joint_target_from_list(self.home_joints)
                self.fetch_state = "HOMING_TO_FETCH"
                self.robot_state = "FETCH_HOMING"
                self.fetch_state_start_time = self.now()
                if self.use_gazebo_sim and self._gazebo_smooth_pos is not None:
                    self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                self._task_smooth_xyz = None
                self._task_target_xyz = None
                self.get_logger().info(
                    f"FETCH CONFIRMED: Homing first, then picking up {obj['name']}")
                return

            if self.fetch_state == "WAITING_RECEIVE":
                if self.fatigue_level in ["FRESH", "MILD"]:
                    self.target_pos[6] = 0.0
                    self.command_gripper(0.0)  # GAZEBO FIX
                    self.get_logger().info(
                        "FETCH: Object released immediately (operator is alert)")
                    self.reset_fetch()
                else:
                    if not hasattr(self, '_handover_first_okay'):
                        self._handover_first_okay = True
                        self.get_logger().warn(
                            "FETCH: Fatigued operator — holding object. "
                            "Show OKAY again to confirm grip.")
                        self.robot_state = "FETCH_CONFIRM_GRIP"
                        return
                    else:
                        self.target_pos[6] = 0.0
                        self.command_gripper(0.0)  # GAZEBO FIX
                        self.get_logger().info(
                            "FETCH: Grip confirmed — releasing to fatigued operator")
                        del self._handover_first_okay
                        self.reset_fetch()
                return

            if self.fatigue_confirmation_pending and self.fatigue_pending_action:
                x, y, z = self.fatigue_pending_action
                self.solve_and_update(x, y, z)
                self.fatigue_confirmation_pending = False
                self.fatigue_pending_action = None
                self.robot_state = "MOVING"
                self.fatigue_confirm_time = self.now()  # GAZEBO FIX
                self.get_logger().info("FATIGUE CONFIRM: Movement approved — 3s grace window")
                return

            if self.fetch_state == "NONE" and self.pending_target:
                x, y, z = self.pending_target
                self.solve_and_update(x, y, z)
                self.pending_target = None
                self.robot_state = "MOVING"

        elif gesture == "GRASP":
            if self.fetch_state == "WAITING_GRASP":
                # Attach FIRST (instant weld), then gently close fingers
                self._attach_object_to_gripper()
                # Gazebo: LinkAttacher weld holds the cube: close gently (0.03)
                # so finger collision mesh doesn't overlap and kick the cube out.
                # Real: no weld exists: must physically close on the object (0.80).
                grasp_close = 0.03 if self.use_gazebo_sim else 0.80
                self.target_pos[6] = grasp_close
                self.command_gripper(grasp_close)
                self.fetch_state = "GRASPING"
                self.robot_state = "FETCH_GRASPING"
                self.fetch_state_start_time = self.now()
                # Kill residual smooth_pos drift before weld
                if self.use_gazebo_sim and self._gazebo_smooth_pos is not None:
                    self._gazebo_smooth_pos = list(self.actual_robot_pos[:6])
                self.get_logger().info("FETCH: Grasping object!")
                return

            # Don't close gripper during ANY active fetch state
            if self.fetch_state != "NONE":
                return

            self.robot_state = "GRASPING"
            self.target_pos[6] = 0.80
            self.command_gripper(0.80)
            self.get_logger().info("GRASP — Gripper command sent")

        elif gesture == "NEUTRAL":
            # Gamepad owns the gripper in torch mode, ignore camera
            if self._torch_override:
                return
            # Don't change gripper while in any active fetch state
            if self.fetch_state not in ["GRASPING", "RETURNING_HOME",
                                         "DELIVERING", "WAITING_RECEIVE",
                                         "APPROACHING", "WAITING_GRASP"]:
                self.target_pos[6] = 0.0
                self.command_gripper(0.0)

    # Landmark callback

    def landmark_callback(self, msg: Float32MultiArray):
        if not self.robot_ready or self.frozen:
            return
        if self.current_gesture != "POINT":
            return

        if self.fatigue_level == "SEVERE":
            if not self.fatigue_lockout_announced:
                self.fatigue_lockout_announced = True
            self.robot_state = "FATIGUE_LOCKOUT"
            return

        data = msg.data
        if len(data) < 27:
            return

        # TORCH mode is handled entirely by _torch_joy_cb + update_loop.
        # Skip landmark processing in torch mode.
        if self._torch_override:
            return

        # In TF tracking mode (Scenario 2), landmark_callback only handles
        # webcam-style object detection. Robot movement is driven by TF in
        # update_loop. When FETCH mode is active (_torch_override=False),
        # the robot waits for ray hover -> OKAY, don't follow the hand.
        if self.use_tf_tracking:
            return

        # FETCH MODE: full pipeline (prediction, geofence, objects)

        if self.fetch_state in ["HOMING_TO_FETCH", "PRE_APPROACHING", "APPROACHING", "WAITING_GRASP",
                                 "GRASPING", "RETURNING_HOME", "DELIVERING",
                                 "WAITING_RECEIVE"]:
            return

        tip_x = data[24]
        tip_y = data[25]

        # Knuckle (index MCP, landmark 5): stable anchor used for object selection.
        # The tip sweeps across cubes when the finger curls into OKAY; the knuckle doesn't.
        knuckle_x = data[15]
        knuckle_y = data[16]

        # Smooth the raw fingertip position BEFORE IK, prevents IK from seeing
        # noisy jumps and producing discontinuous joint solutions. Applied on
        # both sim and real hardware: on the real UR3, 1-pixel webcam jitter
        # near the y-axis singularity caused base-joint swings and stick-slip
        # stutter. alpha=0.20 keeps tracking tight while killing pixel noise.
        tip_alpha = 0.20
        if not hasattr(self, '_smooth_tip_x'):
            self._smooth_tip_x = tip_x
            self._smooth_tip_y = tip_y
        self._smooth_tip_x += tip_alpha * (tip_x - self._smooth_tip_x)
        self._smooth_tip_y += tip_alpha * (tip_y - self._smooth_tip_y)
        tip_x = self._smooth_tip_x
        tip_y = self._smooth_tip_y

        map_offset_x = self.get_parameter('map_offset_x').value
        map_scale_x = self.get_parameter('map_scale_x').value
        map_scale_y = self.get_parameter('map_scale_y').value
        map_scale_z = self.get_parameter('map_scale_z').value
        map_offset_z = self.get_parameter('map_offset_z').value

        if self.use_virtual_operator:
            # Virtual operator / gamepad: all 3 axes from right hand
            # data[26] = depth (W/S), data[24] = tip_x (A/D), data[25] = tip_y (E/C)
            depth = data[26]
            if not hasattr(self, '_smooth_depth'):
                self._smooth_depth = depth
            self._smooth_depth += tip_alpha * (depth - self._smooth_depth)
            robot_x = 0.10 + self._smooth_depth * 0.45   # depth 0->0.10, 0.5->0.325, 1->0.55
            robot_y = -(tip_x - 0.5) * map_scale_y
            robot_z = (0.5 - tip_y) * map_scale_z + map_offset_z

            # Fast-tracking coords for object detection (alpha=0.50)
            # IK uses slow smoothing (0.10) for smooth arm motion, but detection
            # needs to keep up with the operator's finger so cube switching feels responsive.
            raw_tip_x = data[24]
            raw_depth = data[26]
            if not hasattr(self, '_det_tip_x'):
                self._det_tip_x = raw_tip_x
                self._det_depth = raw_depth
            det_alpha = 0.50
            self._det_tip_x += det_alpha * (raw_tip_x - self._det_tip_x)
            self._det_depth += det_alpha * (raw_depth - self._det_depth)
            self._det_robot_x = 0.10 + self._det_depth * 0.45
            self._det_robot_y = -(self._det_tip_x - 0.5) * map_scale_y
        elif self.use_depth_camera:
            # Single-hand 3D mode (depth camera stub, same as legacy 2D for now)
            robot_x = map_offset_x
            robot_y = -(tip_x - 0.5) * map_scale_y
            robot_z = (tip_y - 0.5) * map_scale_z + map_offset_z
            self._det_robot_x = map_offset_x
            self._det_robot_y = -(knuckle_x - 0.5) * map_scale_y
        elif self.camera_placement == 'overhead':
            # Bimanual overhead: right hand -> X,Y tabletop; left hand -> Z 
            # Prefer the homography loaded from table_calibration.yaml: it
            # corrects the tripod's perspective warp (trapezoid view of the
            # table) that the linear map_scale_x/y can only approximate.
            # Falls back to the linear math when no calibration is present.
            rxy = self._homography_image_to_robot_xy(tip_x, tip_y)
            if rxy is not None:
                robot_x, robot_y = rxy
            else:
                robot_x = -(tip_y - 0.5) * map_scale_x + map_offset_x
                robot_y = -(tip_x - 0.5) * map_scale_y
            robot_z = self._bimanual_z
            kxy = self._homography_image_to_robot_xy(knuckle_x, knuckle_y)
            if kxy is not None:
                self._det_robot_x, self._det_robot_y = kxy
            else:
                self._det_robot_x = -(knuckle_y - 0.5) * map_scale_x + map_offset_x
                self._det_robot_y = -(knuckle_x - 0.5) * map_scale_y
        else:
            # Bimanual front_facing: right hand -> Y,Z; left hand -> X depth
            robot_x = self._bimanual_x
            robot_y = -(tip_x - 0.5) * map_scale_y
            robot_z = (tip_y - 0.5) * map_scale_z + map_offset_z
            self._det_robot_x = self._bimanual_x
            self._det_robot_y = -(knuckle_x - 0.5) * map_scale_y

        cx, cy, cz, was_clamped = self.clamp_to_workspace(robot_x, robot_y, robot_z)

        # Object detection BEFORE geofence 
        # Objects sit on the table outside the arm's hover zone,
        # but the operator must still be able to POINT at them from any height.
        # Skip proximity auto-detect in TF tracking mode (Scenario 2),
        # object selection uses ray hover -> OKAY instead.
        det_x = getattr(self, '_det_robot_x', robot_x)
        det_y = getattr(self, '_det_robot_y', robot_y)

        if not self.use_tf_tracking and not getattr(self, '_torch_active', False):
            if self.fetch_state == "NONE":
                obj_idx = self.find_nearest_object(det_x, det_y, 0.0)
                if obj_idx >= 0:
                    obj = self.registered_objects[obj_idx]
                    self.fetch_state = "IDENTIFIED"
                    self.fetch_object_id = obj_idx
                    self.fetch_state_start_time = self.now()
                    self.robot_state = "FETCH_IDENTIFIED"
                    self.get_logger().info(
                        f"FETCH: {obj['name']} identified — show OKAY to confirm")
                    return
            elif self.fetch_state == "IDENTIFIED" and self.current_gesture == "POINT":
                obj_idx = self.find_nearest_object(det_x, det_y, 0.0, sticky=True)
                if obj_idx < 0:
                    self.fetch_state = "NONE"
                    self.fetch_object_id = -1
                    self.robot_state = "IDLE"
                elif obj_idx != self.fetch_object_id:
                    self.fetch_object_id = obj_idx
                    self.fetch_state_start_time = self.now()
                    obj = self.registered_objects[obj_idx]
                    self.get_logger().info(f"FETCH: Switched to {obj['name']}")

        # ── Geofence: clamp to reach sphere instead of rejecting ──
        hand_dist = math.sqrt(robot_x**2 + robot_y**2 + robot_z**2)
        self._hand_inside_bubble = hand_dist <= self.MAX_REACH
        if not self._hand_inside_bubble and hand_dist > 1e-6:
            scale = self.MAX_REACH / hand_dist
            robot_x *= scale
            robot_y *= scale
            robot_z *= scale
            cx, cy, cz, was_clamped = self.clamp_to_workspace(robot_x, robot_y, robot_z)

        in_hazard, zone_name, edge_x, edge_y = self.check_hazard_zones(cx, cy, cz)

        if in_hazard:
            self.publish_target_markers(robot_x, robot_y, robot_z, cx, cy, cz, True)
            self.solve_and_update(edge_x, edge_y, cz)
            self.start_headshake()
            self.robot_state = "HAZARD_DENIED"
            self._hazard_denied = True
            return
        else:
            self._hazard_denied = False

        self.publish_target_markers(robot_x, robot_y, robot_z, cx, cy, cz, was_clamped)

        # Dead zone check: tighter in Gazebo (alpha smoothing handles noise instead)
        # NOTE: dead zone only skips the IK solve, NOT the dwell timer.
        # The dwell timer must keep ticking even when the hand is still.
        dead_zone = 0.02 if self.use_gazebo_sim else 0.04
        in_dead_zone = False
        if hasattr(self, '_last_tip_x'):
            dx = abs(tip_x - self._last_tip_x)
            dy = abs(tip_y - self._last_tip_y)
            if dx < dead_zone and dy < dead_zone:
                in_dead_zone = True
        self._last_tip_x = tip_x
        self._last_tip_y = tip_y

        # Prediction blending
        final_x, final_y, final_z = cx, cy, cz

        if self.ghost_target is not None and self.prediction_confidence > 0.1:
            blend = 0.2 * self.prediction_confidence
            gx, gy, gz = self.ghost_target
            final_x = (1.0 - blend) * cx + blend * gx
            final_y = (1.0 - blend) * cy + blend * gy
            final_z = (1.0 - blend) * cz + blend * gz
            final_x, final_y, final_z, _ = self.clamp_to_workspace(
                final_x, final_y, final_z)

        if self.fetch_state in ["NONE"]:
            # ── Torch Mode / Dwell Timer Logic ────────────────────
            if self._torch_active:
                # Torch mode is ON: continuous tracking (respect fatigue).
                # Dead zone is skipped here: the velocity slew limiter and
                # EMA landmark smoothing already absorb camera jitter, and
                # the dead zone was swallowing fine motor movements.
                if self.fatigue_level == "MODERATE":
                    if self.now() - self.fatigue_confirm_time < 3.0:
                        self.robot_state = "TORCH"
                        self.solve_and_update(final_x, final_y, final_z)
                    else:
                        # Fatigue kicked in: exit torch, require confirmation
                        self._torch_active = False
                        self._dwell_start_time = None
                        self.fatigue_confirmation_pending = True
                        self.fatigue_pending_action = (final_x, final_y, final_z)
                        self.robot_state = "AWAITING_CONFIRMATION"
                else:
                    self.robot_state = "TORCH"
                    self.solve_and_update(final_x, final_y, final_z)
            else:
                # Torch override (gamepad Back button): skip dwell entirely
                if self._torch_override:
                    self._torch_active = True
                    self.robot_state = "TORCH"
                    self.solve_and_update(final_x, final_y, final_z)
                # Torch mode is OFF: manage dwell timer
                elif self._dwell_start_time is None:
                    # First frame of POINT at empty space, start dwell
                    self._dwell_start_time = self.now()
                    self._dwell_target = (final_x, final_y, final_z)
                    self.pending_target = (final_x, final_y, final_z)
                    self.robot_state = "DWELL_WAITING"
                else:
                    # Check if hand drifted too far (velocity reset)
                    dx = abs(final_x - self._dwell_target[0])
                    dy = abs(final_y - self._dwell_target[1])
                    dz = abs(final_z - self._dwell_target[2])
                    drift = math.sqrt(dx**2 + dy**2 + dz**2)

                    if drift > self._dwell_threshold:
                        # Hand moved: reset dwell timer
                        self._dwell_start_time = self.now()
                        self._dwell_target = (final_x, final_y, final_z)
                        self.pending_target = (final_x, final_y, final_z)
                        self.robot_state = "DWELL_WAITING"
                    else:
                        # Hand is still: check if dwell time exceeded
                        elapsed = self.now() - self._dwell_start_time
                        if elapsed >= self.TORCH_DWELL_TIME:
                            # Torch mode ignites!
                            self._torch_active = True
                            self.robot_state = "TORCH"
                            self.solve_and_update(final_x, final_y, final_z)
                            self.get_logger().info(
                                "TORCH MODE — continuous finger tracking active")
                        else:
                            # Still waiting: update pending target
                            self.pending_target = (final_x, final_y, final_z)
                            self.robot_state = "DWELL_WAITING"

        self.prediction_direction = [
            final_x - cx, final_y - cy, final_z - cz]
        self.last_real_target = [cx, cy, cz]


    # Table-calibration homography loader

    def _load_table_calibration(self):
        """Load YAML written by table_calibrator. Silent if missing."""
        path = os.path.join(
            os.path.expanduser('~'), 'ros2_ws', 'data', 'table_calibration.yaml')
        if not os.path.exists(path):
            self.get_logger().info(
                "No table_calibration.yaml — overhead mapping uses linear "
                "map_scale_x/y fallback.")
            return
        try:
            import yaml
            import numpy as np
            with open(path, 'r') as f:
                data = yaml.safe_load(f)
            H = data.get('homography')
            if H is None:
                self.get_logger().warn(
                    f"{path} has no 'homography' key — ignoring.")
                return
            self._homography = np.array(H, dtype=np.float64)
            img_size = data.get('image_size', [1280, 720])
            self._calib_W = float(img_size[0])
            self._calib_H = float(img_size[1])
            self.get_logger().info(
                f"Loaded table homography from {path} "
                f"(calib size {int(self._calib_W)}x{int(self._calib_H)}) — "
                "overhead mapping active.")
        except Exception as e:
            self.get_logger().warn(f"Failed to load {path}: {e}")
            self._homography = None

    def _homography_image_to_robot_xy(self, norm_x, norm_y):
        """Map normalized fingertip (0-1) through saved homography -> robot XY.
        norm_x, norm_y are MediaPipe-normalized image coords (0-1).
        """
        if self._homography is None:
            return None
        try:
            import numpy as np
            # Re-scale to the image size the calibration was done at.
            # We stored corners in pixels, but the homography maps those
            # pixel coords -> robot_xy, so we must hit it with pixel coords.
            # The calibrator saved image_size; for a simple, drift-free
            # transform, multiply by the calibrator's reference size.
            px = norm_x * self._calib_W
            py = norm_y * self._calib_H
            v = np.array([[px, py, 1.0]], dtype=np.float64).T
            out = self._homography @ v
            w = out[2, 0]
            if abs(w) < 1e-9:
                return None
            return float(out[0, 0] / w), float(out[1, 0] / w)
        except Exception:
            return None


    # Left-hand callback (bimanual 2D mode only)

    def left_landmark_callback(self, msg: Float32MultiArray):
        """
        Left hand controls the axis that the 2D camera cannot provide alone:
          - camera_placement='overhead'    -> left hand sets robot Z (height)
          - camera_placement='front_facing' -> left hand sets robot X (depth)
        Only active when use_depth_camera=False and right hand is POINT gesture.
        """
        if self.use_depth_camera or self.use_virtual_operator:
            return  # virtual operator / gamepad uses all 3 axes from one hand
        if not self.robot_ready or self.frozen:
            return
        if self.current_gesture != "POINT":
            return

        data = msg.data
        if len(data) < 27:
            return

        tip_x = data[24]   # index finger tip x  (landmark 8)
        tip_y = data[25]   # index finger tip y

        map_scale_z = self.get_parameter('map_scale_z').value
        map_offset_z = self.get_parameter('map_offset_z').value
        map_scale_y = self.get_parameter('map_scale_y').value
        map_offset_x = self.get_parameter('map_offset_x').value

        if self.camera_placement == 'overhead':
            # Left hand tip_y: top-of-frame = high, bottom = low -> robot Z
            # tip_y=0 -> max Z,  tip_y=1 -> min Z
            raw_z = -(tip_y - 0.5) * map_scale_z + map_offset_z
            self._smooth_left_z += 0.08 * (raw_z - self._smooth_left_z)
            self._bimanual_z = self._smooth_left_z
        else:
            # front_facing: left hand tip_y acts as a depth slider
            # tip_y=0 (hand raised high) -> arm extends further (larger X)
            # tip_y=1 (hand lowered)     -> arm closer (smaller X)
            x_min = 0.15
            x_max = 0.55
            raw_x = x_min + (1.0 - tip_y) * (x_max - x_min)
            self._smooth_left_x += 0.08 * (raw_x - self._smooth_left_x)
            self._bimanual_x = self._smooth_left_x


    # Velocity callback

    def _decay_stale_prediction(self):
        if self._last_velocity_cb_time == 0.0:
            return
        if self.now() - self._last_velocity_cb_time > 0.5:
            # During an active fetch the robot is the one committed,
            # not the hand — keep confidence at 100% even if the
            # operator drops their hand mid-task.
            if self.fetch_state != "NONE":
                self.prediction_confidence = 1.0
            else:
                self.prediction_confidence = 0.0
            self.ghost_target = None

    def velocity_callback(self, msg: Float32MultiArray):
        if self._torch_override:
            return
        if len(msg.data) < 7:
            return
        vel_x, vel_y, vel_z = msg.data[0], msg.data[1], msg.data[2]
        speed = msg.data[3]
        pred_x, pred_y, pred_z = msg.data[4], msg.data[5], msg.data[6]

        map_scale_y = self.get_parameter('map_scale_y').value
        map_scale_z = self.get_parameter('map_scale_z').value
        map_offset_z = self.get_parameter('map_offset_z').value

        # Mirror the landmark_callback mapping so the ghost lands near the
        # fingertip. Previously this path hard-coded front-facing math and
        # the ghost drifted off the table in overhead mode.
        if self.camera_placement == 'overhead':
            rxy = self._homography_image_to_robot_xy(pred_x, pred_y)
            if rxy is not None:
                pred_robot_x, pred_robot_y = rxy
            else:
                map_scale_x = self.get_parameter('map_scale_x').value
                map_offset_x = self.get_parameter('map_offset_x').value
                pred_robot_x = -(pred_y - 0.5) * map_scale_x + map_offset_x
                pred_robot_y = -(pred_x - 0.5) * map_scale_y
            pred_robot_z = getattr(self, '_bimanual_z', map_offset_z)
        else:
            pred_robot_x = getattr(self, '_bimanual_x', self.last_real_target[0])
            pred_robot_y = -(pred_x - 0.5) * map_scale_y
            pred_robot_z = (pred_y - 0.5) * map_scale_z + map_offset_z
        self._last_velocity_cb_time = self.now()

        clamped_x, clamped_y, clamped_z, _ = self.clamp_to_workspace(
            pred_robot_x, pred_robot_y, pred_robot_z)

        # EMA-smooth the ghost target position. Raw prediction = pos + vel*0.5
        # where vel is instantaneous per-frame delta (noisy). Smoothing here
        # eliminates the pixel-jitter on the purple sphere visualization and
        # also stabilises the prediction blend downstream.
        if not hasattr(self, '_ghost_smooth_xyz'):
            self._ghost_smooth_xyz = (clamped_x, clamped_y, clamped_z)
        gsx, gsy, gsz = self._ghost_smooth_xyz
        gsx += 0.25 * (clamped_x - gsx)
        gsy += 0.25 * (clamped_y - gsy)
        gsz += 0.25 * (clamped_z - gsz)
        self._ghost_smooth_xyz = (gsx, gsy, gsz)

        # Confidence semantics:
        #   - Fetch active (object locked) -> 100% baseline
        #   - Free hand-following -> speed-based indicator (divisor tunable)
        #   - Feature 4 gaze gate -> cap at 0.8 during saccade (WoZ Num 6
        #     or real ≥ 100 deg/s movement without a 400 ms fixation)
        # Fail-open: if tobii_node is down, watchdog keeps
        # _gaze_committed = True and the cap is a no-op.
        if self.fetch_state != "NONE":
            self.prediction_confidence = 1.0
        else:
            self.prediction_confidence = min(1.0, speed / 1.0)
        if not self._gaze_committed:
            self.prediction_confidence = min(self.prediction_confidence, 0.8)
        self.ghost_target = (gsx, gsy, gsz)
        self.solve_ghost_ik(gsx, gsy, gsz)
        self.publish_ghost_markers(gsx, gsy, gsz)

        # CSV row for gaze_commitment_timing.py: one row per velocity_callback
        # (~30 Hz matching camera_node cadence).
        if self._gaze_csv_writer is not None:
            try:
                self._gaze_csv_writer.writerow([
                    f"{time.time() - self._gaze_csv_start:.3f}",
                    f"{self._gaze_speed_dps:.3f}",
                    int(self._gaze_is_fixation),
                    f"{self.prediction_confidence:.3f}",
                    f"{speed:.3f}",
                ])
                self._gaze_csv.flush()
            except Exception:
                pass


