    def clamp_to_workspace(self, x, y, z):
        original_x, original_y, original_z = x, y, z
        was_clamped = False
        clamp_reason = []

        # Step 1: clamp height first.
        if z < self.MIN_Z:
            z = self.MIN_Z
            was_clamped = True
            clamp_reason.append(f"Z low ({original_z:.2f}->{z:.2f})")
        elif z > self.MAX_Z:
            z = self.MAX_Z
            was_clamped = True
            clamp_reason.append(f"Z high ({original_z:.2f}->{z:.2f})")

        # Step 2: clamp horizontal reach using the remaining budget after z is set.
        # h_max = sqrt(MAX_REACH² - z²) : the arm sphere after reserving z headroom.
        h = math.sqrt(x**2 + y**2)
        if h < 0.001:
            x = self.MIN_REACH
            y = 0.0
            was_clamped = True
            clamp_reason.append("Too close to center")
        else:
            h_max_sq = self.MAX_REACH**2 - z**2
            if h_max_sq < 0.001:
                # z alone fills the reach sphere: just point straight up
                x, y = 0.0, 0.0
                was_clamped = True
                clamp_reason.append("Z fills reach budget")
            else:
                h_max = math.sqrt(h_max_sq)
                if h > h_max:
                    scale = h_max / h
                    x *= scale
                    y *= scale
                    was_clamped = True
                    clamp_reason.append(
                        f"Reach ({h:.2f}->{h_max:.2f}m horiz at z={z:.2f})")
                elif h < self.MIN_REACH:
                    scale = self.MIN_REACH / h
                    x *= scale
                    y *= scale
                    was_clamped = True
                    clamp_reason.append(f"Too close ({h:.2f}m)")

        if was_clamped:
            self.get_logger().info(f"Target clamped: {', '.join(clamp_reason)}")
        return x, y, z, was_clamped


    # Hazard zone check

    def check_hazard_zones(self, x, y, z):
        """3D cylinder check: blocks the robot at ANY height above the zone."""
        for zone in self.hazard_zones:
            dx = x - zone['x']
            dy = y - zone['y']
            dist_2d = math.sqrt(dx**2 + dy**2)
            if dist_2d < zone['radius']:
                # Push to nearest edge of the cylinder
                if dist_2d < 0.001:
                    edge_x = zone['x'] + zone['radius']
                    edge_y = zone['y']
                else:
                    scale = zone['radius'] / dist_2d
                    edge_x = zone['x'] + dx * scale
                    edge_y = zone['y'] + dy * scale
                return True, zone['name'], edge_x, edge_y
        return False, None, x, y

