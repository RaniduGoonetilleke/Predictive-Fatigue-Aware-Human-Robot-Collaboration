    def _torch_joy_cb(self, msg: Joy):
        """Direct gamepad -> robot position (TORCH) + D-pad object cycling (FETCH)."""
        axes = msg.axes
        buttons = msg.buttons
        if len(axes) < 6 or len(buttons) < 5:
            return

        self._torch_deadman = bool(buttons[4])  # LB

        # Gripper: X button (index 2): edge-triggered only 
        # Gated on fetch_state == "NONE" so the X-release (open) cannot fire
        # during an active fetch and rip the cube out of the gripper.
        if self._torch_deadman and len(buttons) > 2:
            x_pressed = bool(buttons[2])
            x_prev = getattr(self, '_last_x_pressed', False)
            if self.fetch_state == "NONE":
                if x_pressed and not x_prev:
                    self.command_gripper(0.8)  # close on press
                elif not x_pressed and x_prev:
                    self.command_gripper(0.0)  # open on release
            self._last_x_pressed = x_pressed

        # Back button (index 6): toggle torch/fetch directly
        if len(buttons) > 6:
            back_pressed = bool(buttons[6])
            if back_pressed and not getattr(self, '_last_back_pressed', False):
                self._torch_override = not self._torch_override
                if self._torch_override:
                    self._clear_prediction_markers()
                self._publish_mode_marker()
            self._last_back_pressed = back_pressed

        # D-pad object cycling (FETCH mode, Scenario 1)
        if not self.use_tf_tracking and not self._torch_override and len(axes) >= 8:
            dpad_lr = axes[6]  # D-pad left/right: +1=left, -1=right
            prev_dpad = getattr(self, '_prev_dpad_lr', 0.0)
            if abs(dpad_lr) > 0.5 and abs(prev_dpad) < 0.5:
                n = len(self.registered_objects)
                if n > 0:
                    direction = 1 if dpad_lr < 0 else -1  # right=next, left=prev
                    if self.fetch_state == "NONE":
                        # First press: select first/last object
                        idx = 0 if direction == 1 else n - 1
                    else:
                        idx = (self.fetch_object_id + direction) % n
                    self.fetch_object_id = idx
                    self.fetch_state = "IDENTIFIED"
                    self.fetch_state_start_time = self.now()
                    self.robot_state = "FETCH_IDENTIFIED"
                    obj = self.registered_objects[idx]
                    self.get_logger().info(
                        f"FETCH SELECT: {obj['name']} — press OKAY to confirm")
            self._prev_dpad_lr = dpad_lr
            return  # Don't process sticks in FETCH mode

        # Only process sticks in TORCH mode. Store raw deflections and
        # let update_loop integrate them at the true 20Hz dt, integrating
        # here with a hardcoded dt overshoots because joy messages arrive
        # at ~50Hz (F310 publish rate), so _torch_x would accumulate 2.5×
        # faster than the control loop could smooth, blowing the UR3's
        # joint velocity limits on the real robot.
        if not self._torch_override or not self._torch_deadman:
            self._torch_stick_lx = 0.0
            self._torch_stick_ry = 0.0
            self._torch_stick_lt = 0.0
            self._torch_stick_rt = 0.0
            return

        lx = axes[0]
        ry = axes[4]

        # Ghost trigger fix: Linux joy defaults uninitialised triggers
        # to 0.0, which maps to 50% pressed. Ignore until first real input.
        if axes[2] != 0.0:
            self._lt_initialized = True
        if axes[5] != 0.0:
            self._rt_initialized = True
        raw_lt = axes[2] if self._lt_initialized else 1.0
        raw_rt = axes[5] if self._rt_initialized else 1.0
        lt = (1.0 - raw_lt) / 2.0
        rt = (1.0 - raw_rt) / 2.0

        self._torch_stick_lx = lx if abs(lx) > self._torch_deadzone else 0.0
        self._torch_stick_ry = ry if abs(ry) > self._torch_deadzone else 0.0
        self._torch_stick_lt = lt if lt > 0.05 else 0.0
        self._torch_stick_rt = rt if rt > 0.05 else 0.0

