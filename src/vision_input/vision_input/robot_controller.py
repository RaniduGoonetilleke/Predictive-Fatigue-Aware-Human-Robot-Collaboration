    def find_raycast_object(self, sticky=False):
        """Cast a ray along the index finger and find the nearest cube it hits.
        Uses TF: index_middle -> index_distal defines the ray direction.
        Returns object index or -1 if no hit."""
        try:
            tf_mid = self.tf_buffer.lookup_transform(
                'world', 'index_middle', rclpy.time.Time())
            tf_tip = self.tf_buffer.lookup_transform(
                'world', 'index_distal', rclpy.time.Time())
        except Exception:
            return -1

        # Ray origin = fingertip, direction = mid -> tip
        ox = tf_tip.transform.translation.x
        oy = tf_tip.transform.translation.y
        oz = tf_tip.transform.translation.z
        dx = ox - tf_mid.transform.translation.x
        dy = oy - tf_mid.transform.translation.y
        dz = oz - tf_mid.transform.translation.z
        mag = math.sqrt(dx*dx + dy*dy + dz*dz)
        if mag < 1e-6:
            return -1
        dx /= mag
        dy /= mag
        dz /= mag

        # Test each cube: closest distance from ray to cube centre
        RAY_HIT_RADIUS = 0.05  # 5cm hit sphere — must aim at the cube
        best_dist = float('inf')
        best_idx = -1
        for i, obj in enumerate(self.registered_objects):
            # Vector from ray origin to cube centre
            cx, cy, cz = obj['pos']
            vx, vy, vz = cx - ox, cy - oy, cz - oz
            # Project onto ray direction
            t = vx*dx + vy*dy + vz*dz
            if t < 0:
                continue  # cube is behind the finger
            # Perpendicular distance from ray to cube
            px, py, pz = ox + t*dx - cx, oy + t*dy - cy, oz + t*dz - cz
            perp = math.sqrt(px*px + py*py + pz*pz)
            threshold = RAY_HIT_RADIUS * (1.5 if sticky else 1.0)
            if perp < threshold and perp < best_dist:
                best_dist = perp
                best_idx = i
        return best_idx

    def find_nearest_object(self, x, y, z, sticky=False):
        """Y-primary selection band: cubes are arranged along Y axis,
        so lateral distance is the primary discriminator. X (depth) is
        a loose gate: the operator just needs to be pointing over the table.

        sticky=True when already IDENTIFIED, uses a 1.5× wider exit
        threshold to provide hysteresis, preventing rapid selection
        flicker when the hand hovers near the selection boundary.
        """
        X_GATE = 0.25
        best_dy = float('inf')
        best_idx = -1
        for i, obj in enumerate(self.registered_objects):
            dx = abs(x - obj['pos'][0])
            dy = abs(y - obj['pos'][1])
            if dx > X_GATE:
                continue
            if dy < best_dy:
                best_dy = dy
                best_idx = i
        threshold = self.FETCH_PROXIMITY_THRESHOLD * (1.5 if sticky else 1.0)
        if best_dy < threshold:
            return best_idx
        return -1

