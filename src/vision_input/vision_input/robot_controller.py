    def solve_ghost_ik(self, x, y, z):
        # Same analytical UR3 IK as solve_and_update, but writes to ghost_joint_pos.
        wrist_x = x
        wrist_y = y
        wrist_z = z

        d1 = 0.1519
        d4 = 0.11235
        L1 = 0.24365
        L2 = 0.21325

        rho_sq = wrist_x*wrist_x + wrist_y*wrist_y
        min_rho = d4 + 0.005
        if rho_sq < min_rho * min_rho:
            theta = math.atan2(wrist_y, wrist_x) if rho_sq > 1e-6 else 0.0
            wrist_x = min_rho * math.cos(theta)
            wrist_y = min_rho * math.sin(theta)
            rho_sq = min_rho * min_rho

        phi = math.atan2(wrist_y, wrist_x)
        psi = math.atan2(d4, math.sqrt(rho_sq - d4*d4))
        self.ghost_joint_pos[0] = phi - psi

        r = math.sqrt(rho_sq - d4*d4)
        z_local = wrist_z - d1
        d = math.sqrt(r*r + z_local*z_local)

        max_d = L1 + L2 - 0.005
        if d > max_d:
            scale = max_d / d
            r *= scale
            z_local *= scale
            d = max_d
        d = max(0.05, d)

        try:
            cos_elbow = (d*d - L1*L1 - L2*L2) / (2 * L1 * L2)
            cos_elbow = max(-1.0, min(1.0, cos_elbow))
            q3 = math.acos(cos_elbow)
            # UR3 convention: q2 < 0 -> arm up (same fix as _ik_xyz_to_joints)
            target_angle = math.atan2(z_local, r)
            inner_offset = math.atan2(L2*math.sin(q3),
                                      L1 + L2*math.cos(q3))
            q2 = -(target_angle + inner_offset)
            self.ghost_joint_pos[1] = q2
            self.ghost_joint_pos[2] = q3
            self.ghost_joint_pos[3] = -math.pi/2 - q2 - q3
            self.ghost_joint_pos[4] = -math.pi/2
            self.ghost_joint_pos[5] = 0.0
        except ValueError:
            pass


