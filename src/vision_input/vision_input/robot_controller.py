    # Fingertip FK helper

    def _fingertip_from_tf(self, tf_transform):
        """Project tool0 along its Z-axis by GRIPPER_LENGTH to get fingertip position."""
        GRIPPER_LENGTH = 0.15  # metres: tool0 flange -> Robotiq 2F-85 pad tips
        t = tf_transform.transform.translation
        q = tf_transform.transform.rotation
        qx, qy, qz, qw = q.x, q.y, q.z, q.w
        # Tool Z-axis direction in world frame (3rd column of rotation matrix)
        tz_x = 2.0 * (qx * qz + qw * qy)
        tz_y = 2.0 * (qy * qz - qw * qx)
        tz_z = 1.0 - 2.0 * (qx * qx + qy * qy)
        return (
            t.x + GRIPPER_LENGTH * tz_x,
            t.y + GRIPPER_LENGTH * tz_y,
            t.z + GRIPPER_LENGTH * tz_z,
        )


